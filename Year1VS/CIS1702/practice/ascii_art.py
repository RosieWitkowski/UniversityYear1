# Remember to use r for raw text and triple quotation for escaping 
def happy_wizard():
    print(r"""
          *
        /   \
        ^ | ^
          V     *    
        #####  /
    """)

def unhappy_wizard():
    print(r"""
          *
        /   \
        ಠ | ಠ
          ^    *    
        #####  |
    """)

def shocked_wizard():
    print(r"""
            *
          /   \
          O | O
            O     *    
        \ #####  /
    """)

happy_wizard()
unhappy_wizard()
shocked_wizard()
